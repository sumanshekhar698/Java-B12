package com.java.oops.static_conccept;

public class PrimeMinister {

	String name;
	int age;

	@Override
	public String toString() {
		return "PrimeMinister [name=" + name + ", age=" + age + "]";
	}

	public PrimeMinister(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}

}
